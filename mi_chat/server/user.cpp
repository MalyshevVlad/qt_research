#include "user.h"

/*-----------------------------------------------------------------------------------------------*/
User::User() : m_user_name("unknown"), m_socket(nullptr)
{

}
/*-----------------------------------------------------------------------------------------------*/
User::~User()
{

}
/*-----------------------------------------------------------------------------------------------*/
QTcpSocket* User::get_socket()
{
    return m_socket;
}
/*-----------------------------------------------------------------------------------------------*/
QString User::get_user_name()
{
    return m_user_name;
}
/*-----------------------------------------------------------------------------------------------*/
void User::sl_set_user_name(QString _name)
{
    m_user_name = _name;
}
/*-----------------------------------------------------------------------------------------------*/
void User::sl_set_socket(QTcpSocket* _socket)
{
    if (_socket == nullptr)
        delete _socket;
    m_socket = _socket;
}
